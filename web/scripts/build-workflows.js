#!/usr/bin/env node
const fs = require('fs');
const path = require('path');

const root = path.resolve(__dirname, '..', '..');
const workflowDir = path.join(root, 'workflow');
const entries = [];

for (const lang of ['EN', 'ES']) {
  const langDir = path.join(workflowDir, lang);
  const slugs = fs.readdirSync(langDir).filter(d => fs.statSync(path.join(langDir, d)).isDirectory()).sort();

  for (const slug of slugs) {
    const fileName = lang === 'EN' ? 'ORCHESTRATION.md' : 'ORQUESTACION.md';
    const fp = path.join(langDir, slug, fileName);
    if (fs.existsSync(fp) === false) continue;

    const content = fs.readFileSync(fp, 'utf-8');

    const nameMatch = content.match(/^## Workflow\n(.+)$/m);
    const name = nameMatch ? nameMatch[1].trim() : slug;

    const objMatch = content.match(/^## (?:Objective|Objetivo)\n(.+)$/m);
    const objective = objMatch ? objMatch[1].trim() : '';

    entries.push({ lang, slug, path: slug, name, objective, orchestration: content, groupId: '' });
  }
}

// Load existing groupId mappings
const existingFile = path.join(root, 'web/public/workflows.json');
const existing = JSON.parse(fs.readFileSync(existingFile, 'utf-8'));
const groupMap = {};
for (const e of existing) {
  groupMap[e.lang + ':' + e.slug] = e.groupId;
}

// EN<->ES slug mapping for new workflows
const enToEs = {
  'escalation-chain': 'cadena-escalamiento',
  'consensus-voting': 'consenso-votacion',
  'map-reduce': 'map-reduce',
  'triage': 'triage',
  'pair-programming': 'pair-programming',
  'watchdog-supervisor': 'watchdog-supervisor',
  'round-robin': 'round-robin',
  'retry-rollback': 'reintentos-rollback',
  'feedback-loop': 'bucle-retroalimentacion',
  'chain-of-thought': 'cadena-pensamiento',
  'canary-rollout': 'despliegue-canario',
  'workflow-composition': 'composicion-workflows',
  'budget-router': 'ruteo-por-costo'
};
const esToEn = {};
for (const [en, es] of Object.entries(enToEs)) {
  esToEn[es] = en;
}

for (const entry of entries) {
  const key = entry.lang + ':' + entry.slug;
  if (groupMap[key]) {
    entry.groupId = groupMap[key];
  } else if (entry.lang === 'EN') {
    entry.groupId = entry.slug;
  } else {
    entry.groupId = esToEn[entry.slug] || entry.slug;
  }
}

// Write workflows.json
fs.writeFileSync(existingFile, JSON.stringify(entries, null, 2));
console.log('Generated workflows.json with ' + entries.length + ' entries');

// Write workflows-data.ts
const tsOut = path.join(root, 'web/lib/workflows-data.ts');
const tsContent = '// AUTO-GENERATED by scripts/build-registry.js — do not edit manually\nexport const WORKFLOWS_DATA = ' + JSON.stringify(entries, null, 2) + ' as const;\n';
fs.writeFileSync(tsOut, tsContent);
console.log('Generated workflows-data.ts');
